			<p align="justify">Diharapkannya, dukungan Telkom dan kerjasama kedua belah pihak dapat terus ditingkatkan, khususnya dalam mengembangkan potensi Candi Borobudur yang merupakan salah satu destinasi wisata super prioritas.</p>
			<p align="justify">"Semoga kehadiran infrastruktur ini dapat mendorong percepatan pembangunan dan meningkatkan daya saing di era digital," tambah Zainal Arifin.</p>
			<p align="justify">Direktur Consumer Service Telkom, Siti Choiriana menambahkan bahwa adanya infrastruktur yang andal sangat dibutuhkan untuk mendukung Magelang mengembangkan potensinya, khususnya kawasan Candi Borobudur dan area sekitarnya. Selain itu, Siti Choiriana menambahkan adanya infrastruktur 100% fiber optic ini sangat penting bagi IndiHome yang hadir di rumah-rumah keluarga Indonesia.</p>
			<p align="justify">"Melalui IndiHome, siswa dan guru bisa belajar dengan baik. Keluarga bisa lebih produktif dan aktivitas bisnis pun bisa dijalankan dari rumah," jelasnya</p>
			<p align="justify">Hingga saat ini, Telkom telah melakukan modernisasi jaringan di berbagai kota maupun provinsi di Indonesia, sebanyak 228 Modern Broadband City dan pembaharuan teknologi di 1.234 Sentral Telepon Otomat. Sebelumnya juga telah dilakukan peresmian Modern City di Kota Serang dan Kabupaten Lebak.</p>
			<p align="justify">"Meski kita dihadapkan dengan situasi pandemi seperti saat ini, kita harus tetap optimis dan saling bahu membahu. Insya Allah semua akan terlewati dan kondisi akan kembali kondusif. Telkom akan terus memberikan yang terbaik bagi masyarakat Indonesia melalui infrastruktur dan layanan berkualitas," tutup Zulhelfi</p>
