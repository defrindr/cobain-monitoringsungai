            <p align="justify">PT Telkom Indonesia (Persero) Tbk (Telkom) kembali melakukan modernisasi jaringan 100% full fiber sebagai wujud komitmen untuk mempercepat digitalisasi di seluruh Ibukota Kabupaten (IKK) di Indonesia. Melalui layanan video conference, kali ini Direktur Network & IT Solution Telkom, Zulhelfi Abidin bersama Direktur Consumer Service Telkom, Siti Choiriana meresmikan Magelang Modern Broadband City yang ditandai dengan penandatanganan prasasti digital (11/6).</p>
            <p align="justify">Dalam sambutannya Direktur Network & IT Solution Telkom, Zulhelfi Abidin mengatakan di tengah pandemi seperti sekarang ini optimalisasi kualitas jaringan dan layanan Telkom Group menjadi suatu keharusan, salah satunya melalui fiberisasi demi mendukung terwujudnya pengalaman digital terbaik pelanggan, khususnya yang beraktivitas di rumah.</p>
            <p align="justify">"Adanya Magelang Modern Broadband City ini ditujukan untuk mendukung aktivitas pemerintah daerah dan masyarakat Kabupaten Magelang melalui optimalisasi ICT. Tentunya ini akan memantapkan langkah Kabupaten Magelang menjadi Smart City ke depannya." Komitmen Telkom menghadirkan jaringan 100% fiber optic mendapat apresiasi dari Bupati Magelang, Zaenal Arifin,S.IP.</p>
            <p align="justify">"Terima kasih atas dukungan Telkom selama ini sehingga 21 kecamatan, 4 kelurahan, 7 SKPD, Dinas Perhubungan, area lainnya telah terhubung dengan koneksi internet. Bahkan juga tersedia 21 akses wifi di area publik. Keberadaan infrastruktur ini mendukung terwujudnya visi Kabupaten Magelang yang Sedaya Amanah yakni Sejahtera, Berdaya Saing, dan Amanah," ungkap Zaenal Arifin.</p>
			<p><strong><h3>KEMBANGKAN POTENSI WISATA CANDI BOROBUDUR</h3></strong></p>
			<p class="mb-5">
